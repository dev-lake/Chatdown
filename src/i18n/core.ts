const messages: Record<Locale, Record<MessageKey, string>> = {
  en: enMessages,
  'zh-CN': zhCnMessages,
  'zh-TW': zhTwMessages,
  ja: jaMessages,
};

export type { MessageKey, MessageParams };

export type TranslateFn = (key: MessageKey, params?: MessageParams) => string;

export const SUPPORTED_LOCALES: readonly Locale[] = ['en', 'zh-CN', 'zh-TW', 'ja'];

export const LOCALE_OPTIONS: ReadonlyArray<{ value: LocalePreference; labelKey: MessageKey }> = [
  { value: 'auto', labelKey: 'localeAuto' },
  { value: 'en', labelKey: 'localeEnglish' },
  { value: 'zh-CN', labelKey: 'localeSimplifiedChinese' },
  { value: 'zh-TW', labelKey: 'localeTraditionalChinese' },
  { value: 'ja', labelKey: 'localeJapanese' },
];

export function isLocale(value: string): value is Locale {
  return SUPPORTED_LOCALES.includes(value as Locale);
}

export function normalizeLocalePreference(value: unknown): LocalePreference {
  if (value === 'auto') {
    return 'auto';
  }

  if (typeof value === 'string' && isLocale(value)) {
    return value;
  }

  return 'auto';
}

export function getBrowserLanguage(): string {
  if (typeof chrome !== 'undefined' && chrome.i18n?.getUILanguage) {
    return chrome.i18n.getUILanguage();
  }

  if (typeof navigator !== 'undefined' && navigator.language) {
    return navigator.language;
  }

  return 'en';
}

export function resolveLocale(
  preference: LocalePreference = 'auto',
  browserLanguage: string = getBrowserLanguage()
): Locale {
  if (preference !== 'auto') {
    return preference;
  }

  const normalized = browserLanguage.toLowerCase();

  if (normalized.startsWith('ja')) {
    return 'ja';
  }

  if (normalized.startsWith('zh')) {
    if (
      normalized.includes('hant')
      || normalized.includes('tw')
      || normalized.includes('hk')
      || normalized.includes('mo')
    ) {
      return 'zh-TW';
    }

    return 'zh-CN';
  }

  if (normalized.startsWith('en')) {
    return 'en';
  }

  return 'en';
}

export function formatMessage(template: string, params?: MessageParams): string {
  if (!params) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (match, key: string) => {
    const value = params[key];
    return value === undefined ? match : String(value);
  });
}

export function translate(locale: Locale, key: MessageKey, params?: MessageParams): string {
  const template = messages[locale]?.[key] ?? messages.en[key];
  return formatMessage(template, params);
}

export function createTranslator(locale: Locale): TranslateFn {
  return (key, params) => translate(locale, key, params);
}

export async function loadLocalePreference(): Promise<LocalePreference> {
  const result = await chrome.storage.local.get(UI_LOCALE_PREFERENCE_KEY);
  return normalizeLocalePreference(result[UI_LOCALE_PREFERENCE_KEY]);
}

export async function saveLocalePreference(preference: LocalePreference): Promise<void> {
  await chrome.storage.local.set({
    [UI_LOCALE_PREFERENCE_KEY]: preference,
  });
}

export async function getCurrentLocaleContext(): Promise<{
  locale: Locale;
  preference: LocalePreference;
  t: TranslateFn;
}> {
  const preference = await loadLocalePreference();
  const locale = resolveLocale(preference);

  return {
    locale,
    preference,
    t: createTranslator(locale),
  };
}
